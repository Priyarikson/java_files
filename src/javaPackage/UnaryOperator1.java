package javaPackage;

public class UnaryOperator1 {

	public static void main(String[] args) {
		
		int a = 5; // local variable
		
		System.out.println(a); //5
		System.out.println(a++); //5, 6
		System.out.println(a); //6
		System.out.println(++a); //7, 7
		System.out.println(a--); //7
		System.out.println(a); //6

	}

}
